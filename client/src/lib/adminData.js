export const getBookingRows = () => {
  if (typeof window === 'undefined') return defaultBookings;

  const storedBookings = localStorage.getItem(BOOKINGS_STORAGE_KEY);
  if (storedBookings) {
    try {
      const parsed = JSON.parse(storedBookings);
      if (Array.isArray(parsed) && parsed.length > 0) return parsed;
    } catch (e) {
      console.error('Failed to parse saved bookings:', e);
    }
  }

  localStorage.setItem(BOOKINGS_STORAGE_KEY, JSON.stringify(defaultBookings));
  return defaultBookings;
};

export const addBooking = (booking) => {
  if (typeof window === 'undefined') return null;

  const bookings = getBookingRows();
  const nextIdNum = bookings.reduce((max, current) => {
    const num = parseInt((current.id || '').replace('BK-', ''), 10);
    return isNaN(num) ? max : Math.max(max, num);
  }, 2048) + 1;

  const newBooking = {
    id: `BK-${nextIdNum}`,
    customer: booking.customer || 'Guest User',
    movie: booking.movie || 'Unknown movie',
    seats: booking.seats || '',
    total: booking.total || 'LKR 0',
    bookedAt: booking.bookedAt || new Date().toLocaleTimeString('en-US', { hour: 'numeric', minute: '2-digit' }),
    status: booking.status || 'Confirmed',
  };

  const updatedBookings = [newBooking, ...bookings];
  localStorage.setItem(BOOKINGS_STORAGE_KEY, JSON.stringify(updatedBookings));
  window.dispatchEvent(new Event('bookings-updated'));
  return newBooking;
};

